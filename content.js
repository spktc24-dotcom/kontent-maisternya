async function generateContent(cardIndex, context, history = [], refineInstruction = null) {
  const card = cards[cardIndex];
  if (!card) throw new Error('Невірний індекс картки');
  if (!ANTHROPIC_API_KEY) throw new Error('ANTHROPIC_API_KEY не налаштований на сервері');

  const { niche = '', audience = '', offer = '', topic = '', extraFields = {} } = context || {};
  let contextLine = `Ніша: ${niche || 'загальна тематика блогу'}. Аудиторія: ${audience || 'широка аудиторія'}.` +
    (offer ? ` Продукт/проблема: ${offer}.` : '');

  if (topic) {
    contextLine += ` Тема цього конкретного матеріалу: ${topic}.`;
  } else if (card.topicLabel) {
    contextLine += ` Конкретну тему матеріалу користувач не вказав — обери сам найбільш доречну тему для цієї ніші й аудиторії, і явно назви її на початку відповіді.`;
  }

  // Спеціальний випадок: картка каруселей має 4 типи. Замість генерації всіх 4 одразу
  // (дорого по токенах), генеруємо ЛИШЕ один — обраний користувачем або обраний AI сам.
  let taskPrompt = card.prompt;
  if (card.carouselTypes) {
    const chosenType = extraFields.carouselType && card.carouselTypes[extraFields.carouselType]
      ? extraFields.carouselType
      : null;
    if (chosenType) {
      taskPrompt = `Створи структуру каруселі на 6-7 слайдів ТИЛЬКИ типу "${chosenType}" (${card.carouselTypes[chosenType]}). Дай коротку назву кожного слайду і поясни, на який психологічний механізм чи сигнал алгоритму (збереження/dwell time/коментарі) працює ця структура.`;
    } else {
      const typesList = Object.keys(card.carouselTypes).join(', ');
      taskPrompt = `Спочатку сам обери ОДИН найдоречніший тип каруселі з цих чотирьох (${typesList}) для цієї ніші й аудиторії — і явно назви, який тип обрав і чому. Потім створи структуру каруселі на 6-7 слайдів саме цього типу з короткою назвою кожного слайду.`;
    }
  }

  const extraFieldsList = (card.extraFields || []).filter(f => f.key !== 'carouselType');
  if (extraFieldsList.length) {
    const filled = extraFieldsList
      .map(f => ({ label: f.label.replace(' (опційно)', ''), value: (extraFields[f.key] || '').trim() }))
      .filter(f => f.value);
    if (filled.length) {
      contextLine += ' ' + filled.map(f => `${f.label}: ${f.value}.`).join(' ');
    }
    const missing = extraFieldsList.filter(f => !(extraFields[f.key] || '').trim());
    if (missing.length) {
      contextLine += ` Поля, які користувач не заповнив (${missing.map(f => f.label.replace(' (опційно)', '')).join(', ')}) — обери сам найбільш доречні значення, виходячи з ніші й аудиторії.`;
    }
  }

  let historyBlock = '';
  if (refineInstruction && history.length > 0) {
    const lastResult = history[history.length - 1];
    historyBlock = `\n\nВАЖЛИВО: користувач просить ДООПРАЦЮВАТИ вже згенерований результат, а не робити новий з нуля. Ось поточна версія:\n\n${lastResult}\n\nЗапит на зміну від користувача: "${refineInstruction}"\n\nПерепиши цей результат з урахуванням запиту користувача, зберігаючи все, що не стосується запиту, без змін. Видай повну оновлену версію (не тільки змінений фрагмент).`;
  } else if (history && history.length > 0) {
    const prevList = history
      .map((h, i) => `--- Варіант, згенерований раніше (${i + 1}) ---\n${h}`)
      .join('\n\n');
    historyBlock = `\n\nВАЖЛИВО: це вже НЕ перша генерація для цього запиту. Ось усе, що вже було згенеровано раніше для цієї ж ніші й цієї ж картки:\n\n${prevList}\n\nТвоє завдання зараз — дати НАСТУПНУ партію: нові варіанти, які НЕ повторюють попередні ні за змістом, ні за формулюваннями, а розвивають тему далі або пропонують інший ракурс. Не переказуй і не перефразовуй те, що вже було.`;
  }

  const userPrompt = `${contextLine}\n\nЗавдання: ${taskPrompt}${historyBlock}\n\nВідповідай українською, конкретно і практично, без зайвих вступів.`;

  const response = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-api-key': ANTHROPIC_API_KEY,
      'anthropic-version': '2023-06-01'
    },
    body: JSON.stringify({
      model: 'claude-haiku-4-5',
      max_tokens: 2000,
      system: SYSTEM_PROMPT,
      messages: [{ role: 'user', content: userPrompt }]
    })
  });

  if (!response.ok) {
    const errText = await response.text();
    throw new Error(`Anthropic API error ${response.status}: ${errText}`);
  }

  const data = await response.json();
  return (data.content || [])
    .filter(b => b.type === 'text')
    .map(b => b.text)
    .join('\n')
    .trim();
}

// Пропонує 3 короткі варіанти теми для картки, коли людина не знає, що написати.
// Використовується для міні-кроку "обери тему" перед основною генерацією.
async function suggestTopics(cardIndex, context) {
  const card = cards[cardIndex];
  if (!card) throw new Error('Невірний індекс картки');
  if (!ANTHROPIC_API_KEY) throw new Error('ANTHROPIC_API_KEY не налаштований на сервері');

  const { niche = '', audience = '', offer = '' } = context || {};
  const contextLine = `Ніша: ${niche || 'загальна тематика блогу'}. Аудиторія: ${audience || 'широка аудиторія'}.` +
    (offer ? ` Продукт/проблема: ${offer}.` : '');

  const prompt = `${contextLine}\n\nЗапропонуй РІВНО 3 короткі теми (кожна 3-7 слів) для матеріалу типу "${card.title}" (${card.desc}). Формат відповіді: рівно 3 рядки, кожен рядок — одна тема, без нумерації, без лапок, без пояснень.`;

  const response = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-api-key': ANTHROPIC_API_KEY,
      'anthropic-version': '2023-06-01'
    },
    body: JSON.stringify({
      model: 'claude-haiku-4-5',
      max_tokens: 150,
      messages: [{ role: 'user', content: prompt }]
    })
  });

  if (!response.ok) {
    const errText = await response.text();
    throw new Error(`Anthropic API error ${response.status}: ${errText}`);
  }

  const data = await response.json();
  const text = (data.content || [])
    .filter(b => b.type === 'text')
    .map(b => b.text)
    .join('\n')
    .trim();

  return text
    .split('\n')
    .map(line => line.replace(/^[\d.\-–—•*)\s]+/, '').trim())
    .filter(Boolean)
    .slice(0, 3);
}

// Надсилає копію будь-якої генерації в Telegram власнику (ADMIN_CHAT_ID).
// Мовчки нічого не робить, якщо BOT_TOKEN або ADMIN_CHAT_ID не задані —
// це не критична функція, і вона ніколи не повинна ламати основну генерацію.
async function notifyTelegram(text, chatId) {
  const BOT_TOKEN = process.env.BOT_TOKEN;
  const targetChatId = chatId || process.env.ADMIN_CHAT_ID;
  if (!BOT_TOKEN || !targetChatId) return;

  try {
    await fetch(`https://api.telegram.org/bot${BOT_TOKEN}/sendMessage`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        chat_id: targetChatId,
        text: text.slice(0, 4000), // Telegram обмежує повідомлення ~4096 символів
        parse_mode: 'Markdown'
      })
    });
  } catch (err) {
    console.error('Не вдалось відправити копію в Telegram:', err.message);
  }
}

